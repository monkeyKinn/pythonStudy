"""
在py中 使用内置的type方法可以检测一个值的类型
"""
num_1 = 100
num_2 = 3.14
str = "牛"
str_list = [1, 2]
str_bool = True
str_tuple = (1, 2, 3)
str_tuple1 = {1: 1, 2: 2, 3: 3}
str_set = {1, 2, 3}

print(type(num_1))
print(type(num_2))
print(type(str))
print(type(str_list))
print(type(str_bool))
print(type(str_tuple))
print(type(str_set))
print(type(str_tuple1))
